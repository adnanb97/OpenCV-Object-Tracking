clc;
clear all; 
videoName = '06-brzoZaklanjanje';
fileID1 = fopen(strcat(strcat('..\BenchmarkResults\Results', videoName),'.mp4boostingMETRIKA2HSV.txt'),'r');
fileID2 = fopen(strcat(strcat('..\BenchmarkResults\Results', videoName),'.mp4csrtMETRIKA2HSV.txt'),'r');
fileID3 = fopen(strcat(strcat('..\BenchmarkResults\Results', videoName),'.mp4kcfMETRIKA2HSV.txt'),'r');
fileID4 = fopen(strcat(strcat('..\BenchmarkResults\Results', videoName),'.mp4medianflowMETRIKA2HSV.txt'),'r');
