plot(t, kcf,  'DisplayName','kcf');
plot(t, medianflow,  'DisplayName','medianflow');
plot(t, mil,  'DisplayName','mil');
plot(t, mosse,  'DisplayName','mosse');
plot(t, tld,  'DisplayName','tld');
